#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*head;

	head = malloc(sizeof(t_list));
	if (!head)
		exit(write(2, "error malloc", 12));
	head -> content = content;
	head -> next = NULL;
	return (head);
}
